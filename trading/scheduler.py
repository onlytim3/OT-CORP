# Pre-trade narration — human-readable story of why a trade is happening
# ---------------------------------------------------------------------------

def _narrate_trade(signal, order_value, portfolio_value, risk_check, context, stop_loss_price=None):
    """Build a human-readable pre-trade narration in natural prose.

    Combines signal data, market context, risk parameters, and position sizing
    into a narrative that explains *why* this trade is being taken.
    """
    sym = signal.symbol
    action = signal.action.upper()
    strength = signal.strength
    pct_of_portfolio = (order_value / portfolio_value * 100) if portfolio_value > 0 else 0

    # Opening sentence
    parts = [
        f"Opening a {action.lower()} position in {sym} for ${order_value:.2f}, "
        f"representing {pct_of_portfolio:.1f}% of the portfolio."
    ]

    # Signal source
    contributing = (signal.data or {}).get("contributing_strategies", [])
    if contributing:
        strat_list = ", ".join(contributing[:4])
        if len(contributing) > 4:
            strat_list += f" and {len(contributing) - 4} more"
        parts.append(
            f"This trade is backed by consensus from {len(contributing)} strategies "
            f"({strat_list}) with a combined signal strength of {strength:.2f}."
        )
    else:
        parts.append(
            f"Signal generated by {signal.strategy} with strength {strength:.2f}."
        )

    # Signal rationale
    if signal.reason:
        # Clean up strategy-internal reasons for readability
        reason = signal.reason
        if len(reason) < 200:
            parts.append(f"{reason}.")

    # Risk & stops
    risk_parts = []
    if stop_loss_price and stop_loss_price > 0:
        risk_parts.append(f"stop loss at ${stop_loss_price:.2f}")
    if risk_check and hasattr(risk_check, 'reason') and risk_check.reason:
        risk_parts.append(risk_check.reason.lower())
    if risk_parts:
        parts.append(f"Risk management: {'; '.join(risk_parts)}.")

    return "\n\n".join(parts)


