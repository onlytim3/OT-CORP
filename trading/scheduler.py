def check_stop_losses():
    """Check all positions for stop-loss and take-profit/trailing-stop triggers."""
    from trading.risk.profit_manager import check_profit_targets

    try:
        account = _get_account()
        positions = _get_positions()

        if not positions:
            return

        tracker = _get_profit_tracker()

        # -- 1. Take-profit / trailing stop checks --
        profit_actions = check_profit_targets(positions, tracker)
        for action in profit_actions:
            symbol = normalize_symbol(action["symbol"])
            reason = action["reason"]
            pos_side = action.get("pos_side", "long")
            exit_side = "buy" if pos_side == "short" else "sell"
            console.print(f"[bold yellow]{action['action'].upper()}: {symbol} ({pos_side}) -- {reason}[/]")
            log_action(
                "profit_mgmt", action["action"],
                symbol=symbol,
                details=reason,
                data={"pnl_pct": action["pnl_pct"], "qty": action["qty"], "pos_side": pos_side},
            )
            try:
                order = _execute_order(symbol, exit_side, qty=action["qty"])
            except Exception as e:
                log.error("Profit management %s failed for %s: %s", exit_side, symbol, e)
                log_action("trade", f"{action['action']}_{exit_side}_failed", symbol=symbol,
                           result="error", details=f"Order execution failed: {e}")
                continue

            if order.get("status") in ("filled", "accepted", "new", "pending_new"):
                exit_price = float(order.get("filled_avg_price") or 0)
                insert_trade(
                    symbol=symbol,
                    side=exit_side,
                    qty=action["qty"],
                    price=exit_price,
                    total=action["qty"] * exit_price,
                    strategy=f"profit_mgmt_{action['action']}",
                    status=order["status"],
                    alpaca_order_id=order.get("id"),
                )
                # Immediately close matching entry trades so open/recent tables stay in sync
                try:
                    from trading.db.store import close_matching_entry_trades
                    close_matching_entry_trades(symbol, exit_price, action["qty"], exit_side=exit_side)
                except Exception as cmt_err:
                    log.warning("close_matching_entry_trades failed for %s: %s", symbol, cmt_err)
                log_action("trade", f"{action['action']}_{exit_side}", symbol=symbol, result=order["status"])
                tracker.remove(symbol)
                try:
                    from trading.monitor.notifications import notify_stop_loss
                    _notify_safe(notify_stop_loss, symbol, action["pnl_pct"] * 100, action["qty"])
                except Exception:
                    pass
            else:
                log.warning("Profit exit order for %s returned status: %s", symbol, order.get("status"))
                log_action("trade", f"{action['action']}_{exit_side}_failed", symbol=symbol,
                           result=order.get("status"), details=f"Order not filled: {order}")

        # -- 2. Hard stop-loss checks --
        for pos in positions:
            pnl_pct = pos.get("unrealized_pnl_pct", 0) / 100 if pos.get("unrealized_pnl_pct") else 0
            if pnl_pct <= -RISK["stop_loss_pct"]:
                symbol = normalize_symbol(pos["symbol"])
                # Skip if already exited by profit manager above
                if any(normalize_symbol(a["symbol"]) == symbol for a in profit_actions):
                    continue

                pos_side = pos.get("side", "long")
                exit_side = "buy" if pos_side == "short" else "sell"
                console.print(f"[bold red]STOP LOSS triggered: {symbol} ({pos_side}) at {pnl_pct*100:.1f}%[/]")
                log_action(
                    "stop_loss", "triggered",
                    symbol=symbol,
                    details=f"P&L: {pnl_pct*100:.1f}% ({pos_side})",
                    data={"pnl_pct": pnl_pct, "qty": pos["qty"], "pos_side": pos_side},
                )
                try:
                    order = _execute_order(symbol, exit_side, qty=pos["qty"])
                except Exception as e:
                    log.error("Stop-loss %s failed for %s: %s", exit_side, symbol, e)
                    log_action("trade", f"stop_loss_{exit_side}_failed", symbol=symbol,
                               result="error", details=f"Order execution failed: {e}")
                    continue

                if order.get("status") in ("filled", "accepted", "new", "pending_new"):
                    sl_price = float(order.get("filled_avg_price") or 0)
                    insert_trade(
                        symbol=symbol,
                        side=exit_side,
                        qty=pos["qty"],
                        price=sl_price,
                        total=pos["qty"] * sl_price,
                        strategy="stop_loss",
                        status=order["status"],
                        alpaca_order_id=order.get("id"),
                    )
                    # Immediately close matching entry trades so open/recent tables stay in sync
                    try:
                        from trading.db.store import close_matching_entry_trades
                        close_matching_entry_trades(symbol, sl_price, pos["qty"], exit_side=exit_side)
                    except Exception as cmt_err:
                        log.warning("close_matching_entry_trades failed for %s: %s", symbol, cmt_err)
                    log_action("trade", f"stop_loss_{exit_side}", symbol=symbol, result=order["status"])
                    tracker.remove(symbol)
                    try:
                        from trading.monitor.notifications import notify_stop_loss
                        _notify_safe(notify_stop_loss, symbol, pnl_pct * 100, pos["qty"])
                    except Exception:
                        pass
                else:
                    log.warning("Stop-loss exit order for %s returned status: %s", symbol, order.get("status"))
                    log_action("trade", f"stop_loss_{exit_side}_failed", symbol=symbol,
                               result=order.get("status"), details=f"Order not filled: {order}")

        # -- 2.5. Passive loss detection — catch positions that slipped through stops --
        try:
            from trading.risk.margin_monitor import check_passive_loss_accumulation
            passive_alerts = check_passive_loss_accumulation(positions, stop_loss_pct=RISK["stop_loss_pct"])
            for alert in passive_alerts:
                symbol = normalize_symbol(alert["symbol"])
                # Skip if already handled by profit mgr or stop-loss above
                if any(normalize_symbol(a["symbol"]) == symbol for a in profit_actions):
                    continue
                if alert["severity"] == "critical":
                    pos_side = alert["side"]
                    exit_side = "buy" if pos_side == "short" else "sell"
                    console.print(f"[bold red]PASSIVE LOSS DETECTED: {symbol} ({pos_side}) at {alert['loss_pct']*100:.1f}%[/]")
                    log_action(
                        "passive_loss", "emergency_close",
                        symbol=symbol,
                        details=alert["reason"],
                        data={"loss_pct": alert["loss_pct"], "qty": alert["qty"], "side": pos_side},
                    )
                    try:
                        order = _execute_order(symbol, exit_side, qty=alert["qty"])
                        if order.get("status") in ("filled", "accepted", "new", "pending_new"):
                            exit_price = float(order.get("filled_avg_price") or 0)
                            insert_trade(
                                symbol=symbol, side=exit_side, qty=alert["qty"],
                                price=exit_price, total=alert["qty"] * exit_price,
                                strategy="passive_loss_recovery", status=order["status"],
                                alpaca_order_id=order.get("id"),
                            )
                            try:
                                from trading.db.store import close_matching_entry_trades
                                close_matching_entry_trades(symbol, exit_price, alert["qty"], exit_side=exit_side)
                            except Exception:
                                pass
                            log_action("trade", "passive_loss_closed", symbol=symbol, result=order["status"])
                            tracker.remove(symbol)
                            console.print(f"[bold red]CLOSED passive loser {symbol} at ${exit_price:.2f}[/]")
                        else:
                            log.warning("Passive loss exit for %s returned status: %s", symbol, order.get("status"))
                            log_action("trade", "passive_loss_close_failed", symbol=symbol,
                                       result=order.get("status"), details=f"Order not filled: {order}")
                    except Exception as e:
                        log.error("Passive loss close failed for %s: %s", symbol, e)
                        log_action("trade", "passive_loss_close_failed", symbol=symbol,
                                   result="error", details=f"Order execution failed: {e}")
                elif alert["severity"] == "warning":
                    log_action("system", "passive_loss_warning", symbol=symbol, details=alert["reason"])
        except Exception as e:
            log.debug("Passive loss detection error: %s", e, exc_info=True)

        # -- 3. Volume dry-up exit check --
        # Close positions where volume has died (the move is over).
        # Only exit if position is profitable or near break-even to avoid
        # compounding losses — deep losers should be handled by stop-loss.
        try:
            from trading.risk.volume_gate import compute_volume_ratio, compute_volume_trend
            from trading.data.aster import alpaca_to_aster

            vol_exit_ratio = RISK.get("volume_exit_ratio", 0.20)
            already_sold = {normalize_symbol(a["symbol"]) for a in profit_actions}
            for pos in positions:
                symbol = normalize_symbol(pos["symbol"])
                if symbol in already_sold:
                    continue
                aster_sym = alpaca_to_aster(symbol)
                if not aster_sym:
                    continue
                ratio = compute_volume_ratio(aster_sym)
                if ratio is None:
                    continue
                trend = compute_volume_trend(aster_sym) or 0.0

                # Exit if volume is below absolute threshold
                # OR if volume is low-ish AND rapidly fading
                volume_dead = ratio < vol_exit_ratio
                volume_fading_fast = ratio < 0.50 and trend < -0.40
                if not volume_dead and not volume_fading_fast:
                    continue

                # Only exit if P&L is not deeply negative (> -2%)
                pnl_pct = pos.get("unrealized_pnl_pct", 0) / 100 if pos.get("unrealized_pnl_pct") else 0
                if pnl_pct < -0.02:
                    log.info("Volume dry for %s (ratio=%.0f%% trend=%.2f) but P&L %.1f%% — keeping for stop-loss",
                             symbol, ratio * 100, trend, pnl_pct * 100)
                    continue

                pos_side = pos.get("side", "long")
                exit_side = "buy" if pos_side == "short" else "sell"
                exit_reason = "fading fast" if volume_fading_fast and not volume_dead else "below threshold"
                console.print(f"[bold yellow]VOLUME EXIT: {symbol} ({pos_side}) — volume {ratio:.0%} ({exit_reason})[/]")
                log_action(
                    "volume_exit", "triggered",
                    symbol=symbol,
                    details=f"Volume {ratio:.0%} trend={trend:+.2f} ({exit_reason}), P&L {pnl_pct*100:.1f}% ({pos_side})",
                    data={"volume_ratio": round(ratio, 3), "volume_trend": round(trend, 3), "pnl_pct": pnl_pct, "qty": pos["qty"], "pos_side": pos_side},
                )
                try:
                    order = _execute_order(symbol, exit_side, qty=pos["qty"])
                except Exception as e:
                    log.error("Volume exit %s failed for %s: %s", exit_side, symbol, e)
                    log_action("trade", f"volume_exit_{exit_side}_failed", symbol=symbol,
                               result="error", details=f"Order execution failed: {e}")
                    continue

                if order.get("status") in ("filled", "accepted", "new", "pending_new"):
                    vol_exit_price = float(order.get("filled_avg_price") or 0)
                    insert_trade(
                        symbol=symbol,
                        side=exit_side,
                        qty=pos["qty"],
                        price=vol_exit_price,
                        total=pos["qty"] * vol_exit_price,
                        strategy="volume_exit",
                        status=order["status"],
                        alpaca_order_id=order.get("id"),
                    )
                    # Immediately close matching entry trades so open/recent tables stay in sync
                    try:
                        from trading.db.store import close_matching_entry_trades
                        close_matching_entry_trades(symbol, vol_exit_price, pos["qty"], exit_side=exit_side)
                    except Exception as cmt_err:
                        log.warning("close_matching_entry_trades failed for %s: %s", symbol, cmt_err)
                    log_action("trade", f"volume_exit_{exit_side}", symbol=symbol, result=order["status"])
                    tracker.remove(symbol)
                    already_sold.add(symbol)
                else:
                    log.warning("Volume exit order for %s returned status: %s", symbol, order.get("status"))
                    log_action("trade", f"volume_exit_{exit_side}_failed", symbol=symbol,
                               result=order.get("status"), details=f"Order not filled: {order}")
        except Exception as e:
            log.debug("Volume exit check error: %s", e, exc_info=True)

        # -- 4. Post-check sync --
        try:
            from trading.execution.sync import sync_positions
            sync_positions()
        except Exception:
            pass

    except Exception as e:
        log_action("error", "stop_loss_check_error", details=str(e))
        log.error("Stop-loss check error: %s", e)
        console.print(f"[red]Stop-loss check error: {e}[/red]")


