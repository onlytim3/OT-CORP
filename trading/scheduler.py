    console.print(f"  Risk auto-tighten:   at 10% drawdown, halt at 18%")
    console.print(f"  Regime adaptation:   defensive posture in bearish markets")
    console.print(f"  Strategy research:   weekly gap analysis + priority queue")
    console.print(f"  Knowledge base:      continuous learning from outcomes")
    console.print(f"  Dashboard:           http://localhost:5050")
    console.print(f"[bold green]{'='*60}[/]\n")

    log.info("Daemon v3.0 started in %s mode, interval=%dh", mode, interval_hours)
    log_action("scheduler", "daemon_started", details=f"Mode: {mode}, Interval: {interval_hours}h, Version: v3.0")

    # Notify on start
    try:
        from trading.monitor.notifications import notify
        _notify_safe(notify, "Daemon Started", f"Trading daemon v3.0 started in {mode} mode", "info")
    except Exception:
        pass

    # Note: AsterDex orders are immediate (market orders) — no orphaned order reconciliation needed
    log.info("AsterDex primary venue — skipping Alpaca order reconciliation")

    # Start WebSocket price feed (best-effort, falls back to REST)
    try:
        from trading.data.ws_feed import start_ws_feed
        from trading.config import ASTER_SYMBOLS
        ws_symbols = [s for s in ASTER_SYMBOLS.values() if s]
        if ws_symbols:
            start_ws_feed(ws_symbols[:20])  # Top 20 symbols
            console.print(f"[dim]WebSocket feed started for {min(len(ws_symbols), 20)} symbols[/dim]")
    except Exception as e:
        log.debug("WebSocket feed start failed (non-fatal, using REST): %s", e)

    # -----------------------------------------------------------------------
    # One-time P&L migrations — run at startup, NOT in trading cycle
    # These hold the DB open for a while; doing them in the cycle blocks
    # record_daily_pnl() and caused the P&L chart to stop updating.
    # -----------------------------------------------------------------------
    try:
        from trading.db.store import get_setting, set_setting, get_db
        initial = float(os.environ.get("INITIAL_CAPITAL", os.environ.get("PAPER_BALANCE", 1000)))

        # Mark v1/v2 as done so they never block the cycle
        if not get_setting("pnl_recalc_done"):
            set_setting("pnl_recalc_done", "true")
        if not get_setting("pnl_recalc_v2_done"):
            set_setting("pnl_recalc_v2_done", "true")

        if not get_setting("pnl_purge_v3_done"):
            log.info("Running one-time P&L v3 purge at startup...")
            with get_db() as db:
                rows = db.execute(
                    "SELECT date, portfolio_value FROM daily_pnl ORDER BY date ASC"
                ).fetchall()
                if rows:
                    prev_val = initial
                    for row in rows:
                        pv = row["portfolio_value"]
                        daily_ret = (pv - prev_val) / prev_val if prev_val > 0 else 0
                        cum_ret = (pv - initial) / initial if initial > 0 else 0
                        db.execute(
                            "UPDATE daily_pnl SET daily_return = ?, cumulative_return = ? WHERE date = ?",
                            (daily_ret, cum_ret, row["date"]),
                        )
                        prev_val = pv
            set_setting("pnl_purge_v3_done", "true")
            log.info("P&L v3 purge complete: recalculated %d records", len(rows) if rows else 0)
    except Exception as e:
        log.error("P&L v3 startup purge failed: %s", e)

    # -----------------------------------------------------------------------
    # One-time orphan trade cleanup — close stale open entries (buy AND
    # sell/short) from before sell-tracking was added.  Futures trading
    # means both sides can be open entries.  Runs once then sets a flag.
    # -----------------------------------------------------------------------
    try:
        if not get_setting("orphan_cleanup_v2_done"):
            from trading.db.store import get_open_trades, close_trade
            log.info("Running one-time orphan trade cleanup v2...")
            open_trades = get_open_trades()
            cleaned = 0
            for entry in open_trades:
                side = entry.get("side", "").lower()
                if side not in ("buy", "sell", "short"):
                    continue
                closing_side = "sell" if side == "buy" else "buy"
                sym = entry.get("symbol", "")
                from trading.db.store import symbol_variants
                variants = symbol_variants(sym)
                placeholders = ",".join("?" for _ in variants)
                entry_ts = entry.get("timestamp", "")
                with get_db() as db:
                    # Check if there's an opposite-side trade (exit) after this entry
                    exit_row = db.execute(
                        f"SELECT price FROM trades WHERE symbol IN ({placeholders}) "
                        f"AND side=? AND timestamp > ? LIMIT 1",
                        variants + [closing_side, entry_ts],
                    ).fetchone()
                    # Or a newer same-side entry (re-entry after unknown close)
                    newer_entry = db.execute(
                        f"SELECT id FROM trades WHERE symbol IN ({placeholders}) "
                        f"AND side=? AND timestamp > ? AND id != ? LIMIT 1",
                        variants + [side, entry_ts, entry["id"]],
                    ).fetchone()
                if exit_row or newer_entry:
                    exit_price = exit_row["price"] if exit_row and exit_row["price"] else (entry.get("price") or 0)
                    entry_price = entry.get("price") or 0
                    qty = entry.get("qty") or 0
                    if entry_price > 0:
                        if side == "buy":
                            pnl = (exit_price - entry_price) * qty
                        else:
                            pnl = (entry_price - exit_price) * qty
                    else:
                        pnl = 0.0
                    close_trade(entry["id"], exit_price, round(pnl, 2))
                    cleaned += 1
            set_setting("orphan_cleanup_v2_done", "true")
            log.info("Orphan cleanup v2 complete: closed %d stale entry trades", cleaned)
    except Exception as e:
        log.error("Orphan cleanup v2 failed: %s", e)

    # -----------------------------------------------------------------------
    # One-time cleanup v3 — fix stale trades caused by symbol format mismatch.
    # Entries stored as "AVAX/USD" but exits as "AVAXUSD" were never paired.
    # Uses pair_trades() with normalized bucketing + broker position check.
    # -----------------------------------------------------------------------
    try:
        if not get_setting("orphan_cleanup_v3_done"):
            log.info("Running one-time orphan cleanup v3 (symbol format mismatch)...")
            from trading.execution.sync import pair_trades
            paired = pair_trades()
            # Also reconcile against current broker positions
            try:
                positions = _get_positions()
                from trading.monitor.web import _reconcile_orphan_trades
                reconciled = _reconcile_orphan_trades(positions)
            except Exception:
                reconciled = 0
            set_setting("orphan_cleanup_v3_done", "true")
            log.info("Orphan cleanup v3 complete: paired %d, reconciled %d", paired, reconciled)
    except Exception as e:
        log.error("Orphan cleanup v3 failed: %s", e)

    # Run startup validation backtest (if enabled)
    _run_startup_validation()

    # Run immediately on start
    run_trading_cycle()

    # ---------------------------------------------------------------------------
    # Startup sync — run any scheduled tasks that should have fired today
    # ---------------------------------------------------------------------------
    try:
        from trading.db.store import get_action_log as _startup_get_al
        _today = datetime.now(timezone.utc).strftime("%Y-%m-%d")
        _today_actions = _startup_get_al(limit=100, category="journal")
        _has_daily = any(
            a.get("timestamp", "")[:10] == _today
            and isinstance(a.get("data"), dict)
            and a["data"].get("type") == "daily"
            for a in _today_actions
        )
        if not isinstance(_today_actions[0].get("data"), dict) if _today_actions else True:
            # data might be JSON string — parse it
            import json as _json_startup
            _has_daily = any(
                a.get("timestamp", "")[:10] == _today
                and (_json_startup.loads(a["data"]) if isinstance(a.get("data"), str) else a.get("data", {})).get("type") == "daily"
                for a in _today_actions
            )
        if not _has_daily:
            log.info("No daily journal for today — generating on startup")
            run_daily_journal()

        # Check if weekly review is due (if today is Sunday and none generated this week)
        now_utc = datetime.now(timezone.utc)
        if now_utc.weekday() == 6:  # Sunday
            _week_start = (now_utc - timedelta(days=1)).strftime("%Y-%m-%d")
            _has_weekly = any(
                a.get("timestamp", "")[:10] >= _week_start
                and ((_json_startup.loads(a["data"]) if isinstance(a.get("data"), str) else a.get("data", {})).get("type") == "weekly" if a.get("data") else False)
                for a in _today_actions
            )
            if not _has_weekly:
                log.info("No weekly review this week — generating on startup")
                run_weekly_review()
    except Exception as e:
        log.debug("Startup journal/review sync failed (non-fatal): %s", e)

    # Schedule recurring tasks
    schedule.every(interval_hours).hours.do(run_trading_cycle)
    schedule.every(15).minutes.do(check_stop_losses)
    schedule.every(30).minutes.do(process_pending_approvals)
    schedule.every().day.at("23:55").do(run_daily_journal)
    schedule.every().sunday.at("00:00").do(run_weekly_review)
    # Position review now runs as part of run_autonomous_cycle() — no separate schedule needed

    # Graceful shutdown via SIGTERM/SIGINT
    _shutdown_requested = False

    def _handle_shutdown(signum, frame):
        nonlocal _shutdown_requested
        _shutdown_requested = True
        sig_name = signal_mod.Signals(signum).name
        log.info("Received %s — completing current cycle before shutdown", sig_name)
        console.print(f"\n[yellow]Received {sig_name} — shutting down gracefully...[/yellow]")

    signal_mod.signal(signal_mod.SIGTERM, _handle_shutdown)
    signal_mod.signal(signal_mod.SIGINT, _handle_shutdown)

    consecutive_errors = 0
    max_consecutive_errors = 10
    backoff_minutes = 5

    try:
        while not _shutdown_requested:
            try:
                schedule.run_pending()
                consecutive_errors = 0  # Reset on success
            except KeyboardInterrupt:
                break
            except Exception as exc:
                consecutive_errors += 1
                log.error("Daemon loop error (%d/%d): %s", consecutive_errors, max_consecutive_errors, exc)
                log_action("error", "daemon_loop", details=f"Error {consecutive_errors}/{max_consecutive_errors}: {exc}")

                if consecutive_errors >= max_consecutive_errors:
                    pause_msg = f"Too many consecutive errors ({consecutive_errors}), pausing {backoff_minutes}m"
                    log.warning(pause_msg)
                    console.print(f"[red]{pause_msg}[/red]")
                    log_action("scheduler", "daemon_backoff", details=pause_msg)
                    try:
                        from trading.monitor.notifications import notify
                        _notify_safe(notify, "Daemon Backoff", pause_msg, "warning")
                    except Exception:
                        pass
                    time.sleep(backoff_minutes * 60)
                    consecutive_errors = 0  # Reset after backoff

            time.sleep(30)
    except KeyboardInterrupt:
        pass

    # Clean shutdown
    log_action("scheduler", "daemon_stopped", details="Graceful shutdown")
    log.info("Daemon stopped gracefully")
    console.print("\n[yellow]Daemon stopped.[/yellow]")
    try:
        from trading.monitor.notifications import notify
        _notify_safe(notify, "Daemon Stopped", "Trading daemon stopped gracefully", "warning")
    except Exception:
        pass
