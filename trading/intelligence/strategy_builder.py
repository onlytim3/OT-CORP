# Fallback: use momentum template for unknown categories
_DEFAULT_CATEGORY = "momentum"


# ---------------------------------------------------------------------------
# Code generation
# ---------------------------------------------------------------------------

def _snake_case(name: str) -> str:
    """Convert a strategy name to snake_case for filenames and class names."""
    # Replace special chars with underscore
    s = re.sub(r'[^a-zA-Z0-9]', '_', name)
    # CamelCase to snake
    s = re.sub(r'([A-Z])', r'_\1', s).lower()
    # Collapse multiple underscores
    s = re.sub(r'_+', '_', s).strip('_')
    return s


def _class_name(snake: str) -> str:
    """Convert snake_case to CamelCase class name."""
    return ''.join(word.capitalize() for word in snake.split('_')) + 'Strategy'


def _map_category(category: str) -> str:
    """Map strategy universe category to template category."""
    category_lower = category.lower()
    if any(k in category_lower for k in ["trend", "breakout", "channel"]):
        return "trend_following"
    elif any(k in category_lower for k in ["mean_reversion", "reversion", "contrarian", "stat_arb"]):
        return "mean_reversion"
    elif any(k in category_lower for k in ["momentum", "relative_strength", "rotation"]):
        return "momentum"
    # Default
    return _DEFAULT_CATEGORY


def generate_strategy_code(
    strategy_name: str,
    description: str,
    category: str,
    priority: int = 5,
    sharpe_range: str = "0.3-1.0",
    complexity: str = "low",
    data_needed: str = "OHLCV",
) -> str:
    """Generate a complete strategy module from a spec."""
    snake = _snake_case(strategy_name)
    cls_name = _class_name(snake)
    template_cat = _map_category(category)
    template = TEMPLATES.get(template_cat, TEMPLATES[_DEFAULT_CATEGORY])

    config_name = snake.upper()
    date = datetime.now(timezone.utc).strftime("%Y-%m-%d")

    # Build config dict
    config_lines = [f'{config_name} = {{']
    for key, default_val in template["config_params"].items():
        config_lines.append(f'    "{key}": {default_val},')
    config_lines.append('}')
    config_str = '\n'.join(config_lines)

    # Build __init__ body
    init_lines = [f'        cfg = {config_name}']
    for key in template["config_params"]:
        init_lines.append(f'        self.{key} = cfg["{key}"]')
    init_lines.append('        self._last_context: dict = {}')
    init_str = '\n'.join(init_lines)

    # Header
    header = _HEADER.format(
        description=description,
        date=date,
        category=category,
        priority=priority,
        sharpe_range=sharpe_range,
        indicators=template["indicators"],
    )

    # Indent the generate body
    gen_body = template["generate_body"]
    # Indent by 8 spaces (inside a method)
    gen_lines = []
    for line in gen_body.splitlines():
        if line.strip():
            gen_lines.append('        ' + line)
        else:
            gen_lines.append('')
    gen_body_indented = '\n'.join(gen_lines)

    code = f'''{header}
{config_str}


@register
class {cls_name}(Strategy):
    """{description}"""

    name = "{snake}"

    def __init__(self):
{init_str}

    def generate_signals(self) -> list[Signal]:
{gen_body_indented}

    def get_market_context(self) -> dict:
        return {{"strategy": self.name, "coins": self._last_context}}
'''
    return code


