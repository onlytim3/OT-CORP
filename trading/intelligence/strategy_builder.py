def _get_deferred_implementations() -> list[dict]:
    """Get strategy implementation requests from the knowledge base."""
    results = search_knowledge("deferred_implementation", limit=20)
    deferred = []
    for r in results:
        if r.get("category") == "deferred_implementation":
            try:
                data = json.loads(r.get("key_rules", "{}"))
            except (json.JSONDecodeError, TypeError):
                data = {}
            deferred.append({
                "id": r.get("id"),
                "title": r.get("title", ""),
                "content": r.get("content", ""),
                "data": data,
                "timestamp": r.get("ingested_at", ""),
            })
    return deferred


def _already_implemented(strategy_name: str) -> bool:
    """Check if a strategy is already registered."""
    snake = _snake_case(strategy_name)
    registered = list_registered()
    return snake in registered


def build_pending_strategies() -> list[dict]:
    """Build strategies from deferred implementation requests.

    Called from the autonomous cycle. Rate-limited to MAX_BUILD_PER_CYCLE
    strategies per invocation.

    Returns list of {"name": str, "file": str, "result": str} for each built.
    """
    built = []
    deferred = _get_deferred_implementations()

    if not deferred:
        return built

    for entry in deferred[:MAX_BUILD_PER_CYCLE]:
        data = entry.get("data", {})
        name = data.get("name", "")
        if not name:
            # Try to extract from title
            title = entry.get("title", "")
            match = re.search(r"Request:\s*(.+?)\s*—", title)
            name = match.group(1) if match else ""
        if not name:
            continue

        snake = _snake_case(name)

        # Skip if already implemented
        if _already_implemented(name):
            log.info("BUILDER: Strategy '%s' already implemented, skipping", name)
            continue

        # Skip if file already exists
        file_path = STRATEGY_DIR / f"{snake}.py"
        if file_path.exists():
            log.info("BUILDER: File %s already exists, skipping", file_path.name)
            continue

        category = data.get("category", "momentum")
        priority = data.get("priority", 5)
        sharpe_range = data.get("sharpe_range", "0.3-1.0")
        complexity = data.get("complexity", "low")
        data_needed = data.get("data_needed", "OHLCV")
        description = entry.get("content", f"{name} strategy")

        try:
            code = generate_strategy_code(
                strategy_name=name,
                description=description[:200],  # Keep docstring reasonable
                category=category,
                priority=priority,
                sharpe_range=sharpe_range,
                complexity=complexity,
                data_needed=data_needed,
            )

            # Write the strategy file
            file_path.write_text(code)

            # Register in STRATEGY_ENABLED (disabled by default — backtest first)
            STRATEGY_ENABLED[snake] = False

            # Log the build
            log_action(
                "strategy_builder", "strategy_generated",
                symbol=snake,
                details=f"Generated {snake}.py from research recommendation (priority {priority}/10, {category})",
                data={
                    "name": name,
                    "snake_name": snake,
                    "category": category,
                    "priority": priority,
                    "sharpe_range": sharpe_range,
                    "file": str(file_path),
                },
            )

            # Update knowledge base
            insert_knowledge(
                title=f"Strategy Built: {name} — {datetime.now(timezone.utc).strftime('%Y-%m-%d')}",
                source="strategy_builder",
                category="strategy_built",
                content=f"Auto-generated {snake}.py from research recommendation. "
                        f"Category: {category}, Priority: {priority}/10. "
                        f"Strategy is DISABLED — awaiting backtest validation.",
                key_rules=json.dumps({"snake_name": snake, "category": category}),
            )

            built.append({
                "name": name,
                "snake_name": snake,
                "file": str(file_path),
                "result": "generated_disabled",
            })

            log.info("STRATEGY BUILDER: Generated %s.py (category=%s, priority=%d) — disabled, awaiting backtest",
                     snake, category, priority)

        except Exception as e:
            log.error("STRATEGY BUILDER: Failed to generate %s: %s", name, e, exc_info=True)
            log_action(
                "strategy_builder", "generation_failed",
                symbol=snake,
                details=f"Failed to generate {name}: {e}",
            )

    return built
